/* 包含头文件 ----------------------------------------------------------------*/
#include "key_drv.h"

u8 KeyNum = 0;

button_t button[2] = {0};

/**
 * 函数功能: 读取按键电平（获取当前按键状态）
 * 输入参数: 无（使用全局结构体数组 button[] 和宏定义 KEY1、KEY2）
 * 返回参数: 无（结果存入 button[index].level）
 * 说    明: 
 *         1. 该函数将实际的硬件引脚电平状态映射到软件按键结构体的 level 成员。
 *         2. KEY1 和 KEY2 是硬件引脚读取宏（例如 HAL_GPIO_ReadPin），返回 0 表示按下，1 表示释放。
 *         3. 采用静态函数，仅在当前文件内调用。
 */
static void ReadKey(void)    //读取按键值
{
    button[0].level = KEY1;    // 按键1：读取 GPIO 电平并存入结构体
    button[1].level = KEY2;    // 按键2：读取 GPIO 电平并存入结构体
}

/**
 * 函数功能: 按键状态机处理（非传统延时消抖，基于扫描计数）
 * 输入参数: 无（使用全局按键结构体数组 button[] 和全局键值 KeyNum）
 * 返回参数: 无（结果存入全局 KeyNum）
 * 说    明: 
 *         1. 该函数实现了一个三状态按键状态机，消除抖动无需阻塞延时。
 *         2. 状态定义：
 *            - 状态 0：等待按键按下。检测到电平为 0（按下）则进入状态 1。
 *            - 状态 1：确认按下或消抖。连续扫描到 50 次（约 50ms，取决于调用频率）低电平则判定为有效长按，
 *              根据索引设置 KeyNum 为 2（按键1长按）或 4（按键2长按），然后进入状态 2。
 *              若在计数未达 50 时电平变高（释放），则判定为短按，设置 KeyNum 为 1（按键1短按）或 3（按键2短按），
 *              并回到状态 0。
 *            - 状态 2：等待按键释放。检测到电平为 1（释放）则回到状态 0。
 *         3. 扫描计数阈值 50 需根据 Key_Scan 的调用频率调整。
 *         4. 该设计可同时处理短按和长按，且长按只在满足阈值后触发一次，不会重复触发。
 *         5. KeyNum 的值由上层应用程序读取并清零，避免重复响应。
 */
void Key_Process(void)
{
    uint8_t index = 0;

    // 遍历两个按键
    for (index = 0; index < 2; index++)
    {
        switch (button[index].status)
        {
            /* 状态 0：等待按键按下 */
            case 0:
                if (button[index].level == 0)           // 检测到按键按下
                {
                    button[index].scan_cnt = 0;         // 清除消抖计数器
                    button[index].status = 1;           // 转入状态 1（确认按下）
                }
                break;

            /* 状态 1：确认按下（消抖+长短判断） */
            case 1:
                if (button[index].level == 0)           // 仍然处于按下状态
                {
                    button[index].scan_cnt++;           // 消抖计数累加
                    if (button[index].scan_cnt >= 50)   // 达到阈值，判定为长按
                    {
                        if (index == 0)
                        {
                            KeyNum = 2;                 // 按键1长按键值
                            button[index].status = 2;   // 进入状态 2（等待释放）
                        }
                        if (index == 1)
                        {
                            KeyNum = 4;                 // 按键2长按键值
                            button[index].status = 2;
                        }
                    }
                }
                else                                    // 提前释放（未达阈值），判定为短按
                {
                    if (button[index].scan_cnt <= 50)   // 计数未超过阈值
                    {
                        if (index == 0)
                        {
                            KeyNum = 1;                 // 按键1短按键值
                            button[index].status = 0;   // 回到状态 0
                        }
                        if (index == 1)
                        {
                            KeyNum = 3;                 // 按键2短按键值
                            button[index].status = 0;
                        }
                    }
                }
                break;

            /* 状态 2：等待按键释放 */
            case 2:
                if (button[index].level == 1)           // 检测到按键释放
                {
                    button[index].status = 0;           // 回到状态 0
                }
                break;
        }
    }
}

/**
 * 函数功能: 按键扫描总函数（读取电平 + 状态处理）
 * 输入参数: 无
 * 返回参数: 无
 * 说    明: 
 *         1. 该函数在定时中断或主循环中周期性调用。
 *         2. 首先调用 ReadKey() 更新硬件电平到软件结构体，然后调用 Key_Process() 进行状态机处理。
 *         3. 调用频率决定了消抖时间和长按判定时间。
 *         4. 外部程序通过检测全局变量 KeyNum 是否为非零来获取键值，处理完毕后需手动清零 KeyNum。
 */
void Key_Scan(void)
{
    ReadKey();      // 读取当前按键电平
    Key_Process();  // 执行按键状态机处理
}
