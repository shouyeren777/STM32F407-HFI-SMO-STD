/* 包含头文件 ----------------------------------------------------------------*/

#include "eangle_drv.h"                           

/**
 * 函数功能: 电角度发生器（开环模式）
 * 输入参数: p - 电角度结构体指针，包含设定速度、采样时间等参数
 * 返回参数: 无
 * 说    明: 根据设定的电角速度，通过积分计算生成连续递增（或递减）的电角度，
 *           用于无传感器开环启动或虚拟角度生成。电角度以标幺值（0~1）表示，一周对应1.0。
 */
void Electrical_Angle_Generator(E_ANGLE_STRUCT *p)
{
    // ------------------------------------------------------------------------
    // 1. 电角度积分更新（位置 = 位置 + 速度 × 时间）
    // ------------------------------------------------------------------------
    // p->ElectricalAngleSpdSet : 设定电角速度（单位：转/秒？通常为Hz或rps）
    // p->Ts                   : 控制周期（秒）
    // 0.01666f = 1.0f / 60.0f，此处用于单位转换：
    //   假定 ElectricalAngleSpdSet 单位是 RPM（转/分钟），乘以 Ts 后需除以60转换为转/秒，
    //   再积分得到转数（小数部分即为电角度标幺值）。
    // 最终累加到 ElectricalAngleSetPU（标幺电角度，范围0~1）。
    p->ElectricalAngleSetPU += (p->Ts * p->ElectricalAngleSpdSet * 0.01666f);

    // ------------------------------------------------------------------------
    // 2. 边界处理：将电角度限制在 [0, 1) 范围内（循环累加）
    // ------------------------------------------------------------------------
    // 当电角度超过上限1.0时，减去1.0，相当于角度绕回0点
    if(p->ElectricalAngleSetPU >= 1)
    {
        p->ElectricalAngleSetPU = p->ElectricalAngleSetPU - 1;
    }
    // 当电角度低于0时，加上1.0（通常发生在设定速度为负时）
    if(p->ElectricalAngleSetPU < 0)
    {
        p->ElectricalAngleSetPU = p->ElectricalAngleSetPU + 1;
    }
}
