/**
  * 函数功能:根据角度计算正弦和余弦值（查表法）
  * 输入参数:angle   输入角度，范围 0 ~ 1.0，对应 0° ~ 360°
  * 输入参数:sinval  输出正弦值（-1.0 ~ 1.0）
  * 输入参数:cosval  输出余弦值（-1.0 ~ 1.0）
  * 返回参数:
  * 说    明:表大小 1024，覆盖 0° ~ 90° 的浮点正弦值，利用对称性计算任意角度
  */
void Calculate_Sin_Cos(float angle, float *sinval, float *cosval)
{
    // 1. 限制输入范围在 0 ~ 1.0 之间（避免越界）
    Amplitude_Limit(&angle, 0, 1.0f);

    // 2. 提取索引与象限
    u16 idx = (u16)(angle * 4095);                  // 0~1023，用于确定象限和表内索引
    u8  quad = idx >> 10;         // 取高2位（位10、位11）作为象限（0~3）
    u16 table_idx = idx & 0x03FF;          // 取低10位作为表内索引（0~1023）

    // 3. 根据象限利用对称性查表并赋予符号
    switch (quad)
    {
    case 0:   // 第一象限  0° ~ 90°
        // sin(θ) = 表[θ]
        // cos(θ) = 表[90°-θ] = 表[1023 - θ]
        *sinval =  Sin_Cos_Table[table_idx];
        *cosval =  Sin_Cos_Table[1023 - table_idx];
        break;

    case 1:   // 第二象限 90° ~ 180°
        // sin(θ) = sin(180°-θ) = 表[180°-θ]，其中180°-θ对应第一象限角度
        // cos(θ) = -cos(180°-θ) = -表[θ-90°] = -表[table_idx]
        *sinval =  Sin_Cos_Table[1023 - table_idx];
        *cosval = -Sin_Cos_Table[table_idx];
        break;

    case 2:   // 第三象限 180° ~ 270°
        // sin(θ) = -sin(θ-180°) = -表[θ-180°]
        // cos(θ) = -cos(θ-180°) = -表[90° - (θ-180°)] = -表[1023 - table_idx]
        *sinval = -Sin_Cos_Table[table_idx];
        *cosval = -Sin_Cos_Table[1023 - table_idx];
        break;

    case 3:   // 第四象限 270° ~ 360°
        // sin(θ) = -sin(360°-θ) = -表[360°-θ]
        // cos(θ) =  cos(360°-θ) = 表[90° - (360°-θ)] = 表[table_idx]
        *sinval = -Sin_Cos_Table[1023 - table_idx];
        *cosval =  Sin_Cos_Table[table_idx];
        break;

    default:
        // 不会进入此分支，仅用于防止编译警告
        *sinval = 0.0f;
        *cosval = 0.0f;
        break;
    }

    // 注意：因为表中直接存储浮点正弦值，无需额外缩放
}

/**
 * 函数功能: 限幅函数（对指针指向的数值进行边界限制）
 * 输入参数: input - 待限幅的浮点数指针（直接修改原值）
 *          min   - 下限值
 *          max   - 上限值
 * 返回参数: 无（通过指针直接修改原值）
 * 说    明: 
 *         1. 将输入数值限制在[min, max]闭区间内。
 *            若 *input < min，则 *input = min；
 *            若 *input > max，则 *input = max；
 *            否则保持不变。
 *         2. 采用指针传递，避免返回值，可直接修改变量原值，效率较高。
 *         3. 典型应用场景：PID输出限幅、电压指令限幅、转速给定限幅等。
 */
void Amplitude_Limit(float *input, float min, float max)
{
    // 若当前值小于下限，则赋值为下限
    if (*input <= min)
    {
        *input = min;
    }

    // 若当前值大于上限，则赋值为上限
    if (*input >= max)
    {
        *input = max;
    }
}





